Vector2::Vector2()
{
	x = 0.0f;
	y = 0.0f;
}
//---------------------------------------------------------------------------------
//	É[Éçèâä˙âª
//---------------------------------------------------------------------------------
void Vector2::clear()
{
	x = 0.0f;
	y = 0.0f;
}
//---------------------------------------------------------------------------------
//	ílÇÃÉZÉbÉg
//---------------------------------------------------------------------------------
void Vector2::set( float x, float y )
{
	this->x = x;
	this->y = y;
}
void Vector2::set( Vector2 &v )
{
	x = v.x;
	y = v.y;
}
//---------------------------------------------------------------------------------
//	ë´ÇµéZ
//---------------------------------------------------------------------------------
void Vector2::add( Vector2 &v )
{
	x += v.x;
	y += v.y;
}
void Vector2::add( Vector2 &v0, Vector2 &v1 )
{
	x = v0.x + v1.x;
	y = v0.y + v1.y;
}
//---------------------------------------------------------------------------------
//	à¯Ç´éZ
//---------------------------------------------------------------------------------
void Vector2::subtract( Vector2 &v )
{
	x -= v.x;
	y -= v.y;
}
void Vector2::subtract( Vector2 &v0, Vector2 &v1 )
{
	x = v0.x - v1.x;
	y = v0.y - v1.y;
}
//---------------------------------------------------------------------------------
//	ä|ÇØéZ
//---------------------------------------------------------------------------------
void Vector2::scale( float scale )
{
	x *= scale;
	y *= scale;
}
//---------------------------------------------------------------------------------
//	í∑Ç≥éÊìæ
//---------------------------------------------------------------------------------
float Vector2::length()
{
	return sqrtf( x * x + y * y );
}
//---------------------------------------------------------------------------------
//	ê≥ãKâª
//---------------------------------------------------------------------------------
void Vector2::normalize()
{
	float len = length();
	if( len == 0.0f ){
		return;
	}
	x = x / len;
	y = y / len;
}
//---------------------------------------------------------------------------------
//	í∑Ç≥ê›íË
//---------------------------------------------------------------------------------
void Vector2::length( float len )
{
	normalize();
	x = x * len;
	y = y * len;
}
