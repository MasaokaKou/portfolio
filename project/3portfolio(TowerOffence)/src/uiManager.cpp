//===========================================================================
//! ユニット
//===========================================================================

//---------------------------------------------------------------------------
//! コンストラクタ
//---------------------------------------------------------------------------
uiManager ::uiManager()
{
    //! 実体生成
    _unit_sel = std::make_unique<unitSelector>();
}

//---------------------------------------------------------------------------
//! デストラクタ
//---------------------------------------------------------------------------
uiManager ::~uiManager()
{
}

//---------------------------------------------------------------------------
//! 初期化
//---------------------------------------------------------------------------
bool uiManager::initialize()
{
    if(_unit_sel)
        _unit_sel.get()->initialize();

    return true;
}

//---------------------------------------------------------------------------
//! 更新
//---------------------------------------------------------------------------
void uiManager::update()
{
    if(_unit_sel) {
        //_unit_sel.get()->setUnitLimit(_unit_limit);
        _unit_sel.get()->update();
       
    }
    
}
//---------------------------------------------------------------------------
//! 描画
//---------------------------------------------------------------------------
void uiManager::render()
{
    if(_unit_sel)
        _unit_sel.get()->render();
}

//---------------------------------------------------------------------------
//! 解放
//---------------------------------------------------------------------------
void uiManager::finalize()
{
    if(_unit_sel)
        _unit_sel.get()->finalize();
}


s16 uiManager::selNum()
{
    if(_unit_sel) {
        return _unit_sel.get()->selNum();
    }
}

//---------------------------------------------------------------------------
//! 各ユニットの上限をもらってくる
//---------------------------------------------------------------------------
void uiManager::setUnitLimit(std::vector<s16> unit_limit_)
{
   
       _unit_sel.get()->setUnitLimit(unit_limit_);
    
}


