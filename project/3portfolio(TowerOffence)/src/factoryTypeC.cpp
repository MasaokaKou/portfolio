#include "FactoryTypeC.h"
//===========================================================================
//! 防衛施設
//===========================================================================

//---------------------------------------------------------------------------
//! コンストラクタ
//---------------------------------------------------------------------------
FactoryTypeC ::FactoryTypeC()
{
    _type       = TYPE::TYPE_C;
    par._hp     = 30;   // 耐久値
    par._atk    = 10;    // 攻撃力
    par._def    = 0;    // 耐久力
    par._atkSpd = 180;   // 攻撃速度

    // 当たり判定用の変数
    _hit._hcPos   = VGet(_pos.x, _pos.y, _pos.z);   // 現在位置
    _hit._hcScale = 15;                             // サイズ

    // 攻撃用当たり判定用の変数
    _hitAtk._hcPos   = VGet(_pos.x, _pos.y, _pos.z);   // 現在位置
    _hitAtk._hcScale = 15;                             // サイズ
    _hitAtk._hcSpace = 0;

    _color = GetColor(0, 0, 255);

    _atk_size = 3;
}

//---------------------------------------------------------------------------
//! デストラクタ
//---------------------------------------------------------------------------
FactoryTypeC ::~FactoryTypeC()
{
}

//---------------------------------------------------------------------------
//! 初期化
//---------------------------------------------------------------------------
bool FactoryTypeC::initialize()
{
    FactoryBase::initialize("data/Tower/tower.mqo", "data/Tower/towerTypeC.png");

    return true;
}

//---------------------------------------------------------------------------
//! 更新
//---------------------------------------------------------------------------
void FactoryTypeC::update()
{
    FactoryBase::update();
    FactoryBase::attackAnimTypeC();
}
//---------------------------------------------------------------------------
//! 描画
//---------------------------------------------------------------------------
void FactoryTypeC::render()
{
    FactoryBase::render();
}

//---------------------------------------------------------------------------
//! 解放
//---------------------------------------------------------------------------
void FactoryTypeC::finalize()
{
    FactoryBase::finalize();
}
